
function Gato() 
{
	if (navigator.appName == "Microsoft Internet Explorer" && (navigator.appVersion.indexOf("MSIE 7")!=-1) || (navigator.appVersion.indexOf("MSIE 8")!=-1))
	{
		if (document.all)
		 {
		  document.all.sound.src = "sounds/espanol/animales/gato.mp3";
		 }
	}

	else {
			{
			var audio = document.getElementById("gato"); audio.play(); 
			audio.play();
			}
		}	
}
